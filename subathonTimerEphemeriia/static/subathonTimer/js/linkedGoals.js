function init() {
  addGoalMarkers();
  updateGoalProgress();
}

function addGoalMarkers() {
  let progressWrapperContainer = document.getElementById("progress-wrapper");
  progressWrapperContainer.style.setProperty(
    "width",
    13.9 * slice_goals * goals.length + "px",
  );
  for (let i = 0; i < goals.length; i++) {
    let goal = goals[i];

    let goalContainer = document.createElement("div");
    goalContainer.className = "goal-container";

    let goalDiv = document.createElement("div");
    goalDiv.className = "goal-marker";
    goalDiv.setAttribute("data-goal-id", goal.id);
    goalContainer.appendChild(goalDiv);

    let goalIcon = document.createElement("div");
    goalIcon.className = "goal-icon";
    goalIcon.innerHTML = starSVG;
    goalDiv.appendChild(goalIcon);

    let linkedBar = document.getElementById("goal-progress-" + goal.id);
    linkedBar.appendChild(goalContainer);
  }
}

function updateGoalProgress() {
  let previousGoal = 0;
  let lockGoal = false;
  let activeIndex = 0;
  for (let i = 0; i < goals.length; i++) {
    let goal = goals[i];
    let goalDiv = document.getElementById("goal-progress-" + goal.id);
    let progress = Math.min(
      100,
      Math.max(
        0,
        ((current_amount - previousGoal) / (goal.goal_amount - previousGoal)) *
          100,
      ),
    );

    previousGoal = goal.goal_amount;
    let progressBar = goalDiv.querySelector(".progress-bar-fill");

    progressBar.style.width = progress + "%";
    if (progress >= 100) {
      validateGoal(goalDiv);
    }
    if (current_amount < goal.goal_amount && !lockGoal) {
      lockGoal = true;

      activeIndex = Math.floor(i / slice_goals) * slice_goals;
      changeNextGoalAmount(goal.goal_amount);
      setNextGoalDisplay(goal.goal_name, goal.goal_amount);
    }
    if (current_amount >= goals[goals.length - 1].goal_amount) {
      changeNextGoalAmount(goals[goals.length - 1].goal_amount);
      setNextGoalDisplay("Fini !", goals[goals.length - 1].goal_amount);
    }
  }

  slideToGoal(activeIndex);
}

function changeNextGoalAmount(goal) {
  nextGoalAmount = goal;
}
function setNextGoalDisplay(title, amount) {
  let nextGoalText = document.getElementById("next-goal");
  let nextGoalAmountText = document.getElementById("goal-amount");
  if (nextGoalText) {
    nextGoalText.textContent = title;
  }
  if (nextGoalAmountText) {
    nextGoalAmountText.textContent = amount / 5;
  }
}

function validateGoal(goalDiv) {
  let markerDiv = goalDiv.querySelector(".goal-marker");
  if (markerDiv) {
    if (!markerDiv.classList.contains("validate-marker")) {
      markerDiv.classList.add("validate-marker");
      addValidateIndicator(markerDiv);
    }
  }
}

function addValidateIndicator(goalDiv) {
  let goalIcon = document.createElement("div");
  goalIcon.className = "goal-icon";
  let oldIcon = goalDiv.querySelector(".goal-icon");
  if (oldIcon) {
    oldIcon.classList.add("vanishing");
  }
  goalIcon.innerHTML = starValidateSVG;

  goalDiv.insertAdjacentElement("afterbegin", goalIcon);

  let goalId = goalDiv.getAttribute("data-goal-id");
  let indicator = goals.find((g) => g.id == goalId).indicator;
  let indicatorDiv = document.createElement("div");
  indicatorDiv.className = "indicator-container";
  let pTag = document.createElement("p");
  pTag.textContent = indicator;
  pTag.className = "goal-indicator";
  indicatorDiv.appendChild(pTag);
  goalDiv.insertAdjacentElement("afterend", indicatorDiv);
}

function slideToGoal(index) {
  const goalWidth = 13.9 * slice_goals;
  const offset = index * goalWidth;
  const wrapper = document.getElementById("progress-wrapper");
  wrapper.style.transform = `translateX(-${offset}px)`;
}

